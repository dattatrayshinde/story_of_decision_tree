library("rpart")
library(rpart.plot)

# ------------------------------------------------------------------------------
# 3. GET DATA
# ------------------------------------------------------------------------------

compLA<- read.csv("DT Loan_approval.csv",header = TRUE)
# ------------------------------------------------------------------------------
# 4. FIT MODEL
# ------------------------------------------------------------------------------
fit<- rpart(formula =  Loan.Approved~Age+Income ,
            method = "class",
            data=compLA, 
            control=rpart.control(minsplit=1, 
            minbucket=1, 
            cp=0.01))
summary(fit)
# ------------------------------------------------------------------------------
# 5. VISUALIZATION
# ------------------------------------------------------------------------------
rpart.plot(fit)
